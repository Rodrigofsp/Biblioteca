public class Livro {

    private String  Titulo, Editora;

    public Livro(String titulo, String editora) {
        Titulo = titulo;
        Editora = editora;

    }

    public String getTitulo() {
        return Titulo;
    }

    public void setTitulo(String titulo) {
        Titulo = titulo;
    }

    public void setEditora(String editora) {
        Editora = editora;
    }

    public String getEditora() {
        return Editora;
    }







}
